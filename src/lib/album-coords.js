// Return [lat, lng, label] for an album, or null if not found.
export function coordsForAlbum(albumId) {
  return ALBUM_COORDS[albumId] ?? null;
}

// Build an array of {albumId, lat, lng, label} for all mapped albums.
export function allMapMarkers() {
  return Object.entries(ALBUM_COORDS).map(([id, [lat, lng, label]]) => ({
    albumId: Number(id),
    lat,
    lng,
    label,
  }));
}
